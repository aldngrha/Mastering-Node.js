/*The division operator produces the quotient of its operands where the left
    operand is the dividend and the right operand is the divisor.*/
console.log(1 / 2); // returns 0.5 in JavaScript
console.log(1.0 / 2.0); // returns 0.5 in both JavaScript and Java
console.log(2.0 / 0); // returns Infinity in JavaScript
console.log(2.0 / 0.0); // returns Infinity too
console.log(2.0 / -0.0); // returns -Infinity in JavaScript
