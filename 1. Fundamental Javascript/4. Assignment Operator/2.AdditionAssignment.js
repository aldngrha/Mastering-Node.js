// Assuming the following variables
//  foo = 'foo'
//  bar = 5
//  baz = true

// Number + Number -> addition
bar += 2; // 7

// Boolean + Number -> addition
baz += 1; // 2

// Boolean + Boolean -> addition
baz += false; // 1

// Number + String -> concatenation
bar += 'foo'; // "5foo"

// String + Boolean -> concatenation
foo += false; // "foofalse"

// String + String -> concatenation
foo += 'bar'; // "foobar"
